      SUBROUTINE SAVER(IY)
C
C  SAVE saves the intermediate Jan 1st runs used for interpolation
C  by ADJUST.
C
C  Called by MOBILE.
C
C  Calls none.
C
C  Input on call:
C
C    paramter list: IY
C    common blocks:
C    /EVAPGR/ EVP,GREVP,VGREVP,PARD,GRPD,
C    /RESUL1/ EFFTP,EFEXH,EFEVAP,EFREFL,EFRUNL
C    /RESUL2/ RLGGAL,RSTGPH,EFRSTL,EFIDLE,
C    /RESUL/ VFTP,VEXH,VEVAP,VLOSS,VRUNLS,VRSTLS,VIDLE
C    /MAXIMA/ MAXVEH,MAXPOL
C    /VMXCOM/ REGMIX,TFNORM,VMTMIX,VCOUNT,
C
C  Output on return:
C
C    common blocks:
C    /X1/ XEVP,XGREVP,XVGREV,XGRPD,
C    /X2/ XFTP,XEXH,XEVAP,XREFL,XRUNL,
C    /X3/ XRLG,XRSTG,XRSTL,XIDLE,
C    /X4/ XVFTP,XVEXH,XVEVAP,XVLOSS,XVRUNL,XVRSTL,XVIDLE,
C    /X5/ XREG,XVMT
C
C  Notes:
C
C  SAVER was added for MOBILE5v1.
C
C
      COMMON /EVAPGR/ EVP(4),GREVP(4,9),VGREVP(4),PARD(3),GRPD(3,9)
      COMMON /RESUL1/ EFFTP(3,9),EFEXH(9),EFEVAP(9),EFREFL(9),EFRUNL(9)
      COMMON /RESUL2/ RLGGAL(9),RSTGPH(9),EFRSTL(9),EFIDLE(3,9)
      COMMON /RESUL3/ VFTP(3),VEXH,VEVAP,VLOSS,VRUNLS,VRSTLS,VIDLE(3)
      COMMON /MAXIMA/ MAXVEH,MAXLTW,MAXPOL,MAXREG,MAXYRS
      COMMON /VMXCOM/ REGMIX(8),TFNORM(8),VMTMIX(8),VCOUNT(39,8)
C
      COMMON /X1/ XEVP(4,2),XGREVP(4,9,2),XVGREV(4,2),XGRPD(3,9,2)
      COMMON /X2/ XFTP(3,9,2),XEXH(9,2),XEVAP(9,2),XREFL(9,2),XRUNL(9,2)
      COMMON /X3/ XRLG(9,2),XRSTG(9,2),XRSTL(9,2),XIDLE(3,9,2)
      COMMON /X4/ XVFTP(3,2),XVEXH(2),XVEVAP(2),XVLOSS(2),XVRUNL(2),
     *            XVRSTL(2),XVIDLE(3,2)
      COMMON /X5/ XREG(8,2),XVMT(8,2)
C
      MAXV = MAXVEH + 1
C
      DO 20 IP = 1, MAXPOL
        XVFTP(IP,IY) = VFTP(IP)
        XVIDLE(IP,IY) = VIDLE(IP)
        DO 10 IV = 1, MAXV
          XFTP(IP,IV,IY) = EFFTP(IP,IV)
          XIDLE(IP,IV,IY) = EFIDLE(IP,IV)
   10   CONTINUE
   20 CONTINUE
C
      DO 30 IV = 1, MAXV
        XEXH(IV,IY) = EFEXH(IV)
        XEVAP(IV,IY) = EFEVAP(IV)
        XREFL(IV,IY) = EFREFL(IV)
        XRUNL(IV,IY) = EFRUNL(IV)
        XRSTL(IV,IY) = EFRSTL(IV)
        XRLG(IV,IY) = RLGGAL(IV)
        XRSTG(IV,IY) = RSTGPH(IV)
   30 CONTINUE
C
      DO 40 IV = 1, MAXVEH
        XREG(IV,IY) = REGMIX(IV)
        XVMT(IV,IY) = VMTMIX(IV)
   40 CONTINUE
C
      XVEVAP(IY) = VEVAP
      XVLOSS(IY) = VLOSS
      XVRUNL(IY) = VRUNLS
      XVRSTL(IY) = VRSTLS
      XVEXH(IY) = VEXH
C
      DO 60 IEVP = 1, 4
        XEVP(IEVP,IY) = EVP(IEVP)
        XVGREV(IEVP,IY) = VGREVP(IEVP)
        DO 50 IV = 1, MAXV
          XGREVP(IEVP,IV,IY) = GREVP(IEVP,IV)
   50   CONTINUE
   60 CONTINUE
C
      DO 80 IV = 1, MAXV
        DO 70 IPARTL = 1, 3
          XGRPD(IPARTL,IV,IY) = GRPD(IPARTL,IV)
   70   CONTINUE
   80 CONTINUE
C
      RETURN
      END
