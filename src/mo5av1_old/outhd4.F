      SUBROUTINE OUTHD4(ICY)
C
C  OUTHD4 writes the run header on unit IOUREP using output format (OUTFMT) 4.
C  The header consists of the run title and a partial echo of user selected
C  options and data.
C
C  Called by OUTPUT.
C
C  Calls OUTATP, OUTIMP, OUTLAP, OUTMTH, OUTNEW, OUTTAM and USDPTR.
C
C  Input on call:
C
C    common blocks:
C    parameter list: ICY
C    /ETPPAR/ NEWETP
C    /FLAGS1/ TAMFLG
C    /FLAGS2/ NEWFLG,IMFLAG
C    /FLAGS3/ ATPFLG
C    /IOUCOM/ IOUREP
C    /PRGCH2/ PRGFLG
C    /PRSCH2/ PRSFLG
C
C  Output on return:
C
C    common block: /REGION/ INITPR
C
C  Notes:
C
C  The call to TAMPER was restored in Version I00.
C
C
      INTEGER PROMPT,TAMFLG,SPDFLG,VMFLAG,OXYFLG,DSFLAG
      INTEGER ALHFLG
      INTEGER ATPFLG,TPDFLG,RLFLAG,TEMFLG,OUTFMT
      INTEGER PRGFLG,PRSFLG
C
      COMMON /ETPPAR/ ETP(7,3),NEWETP,IETPYR(4),ETPPIV(4),NETPYR
      COMMON /FLAGS1/ PROMPT,TAMFLG,SPDFLG,VMFLAG,OXYFLG,DSFLAG
      COMMON /FLAGS2/ MYMRFG,NEWFLG,IMFLAG,ALHFLG,IMTIER
      COMMON /FLAGS3/ ATPFLG,TPDFLG,RLFLAG,LOCFLG,TEMFLG,OUTFMT
      COMMON /IOUCOM/ IOUIMD,IOUGEN,IOUREP,IOUERR,IOUASK
      COMMON /PRGCH2/ PRGPGM,PRGFQT,CRPRG,PRGFLG
      COMMON /PRSCH2/ PRSPGM,PRSFQT,CRPRS,PRSFLG
      COMMON /REGION/ FEET(2),IREJN,ALT,INITPR
C
      INITPR=0
C
      IF(TAMFLG.EQ.2) CALL OUTTAM
C
      IF(NEWFLG.EQ.2.OR.NEWFLG.EQ.4.OR.NEWFLG.EQ.6) CALL OUTNEW
C
      IF(NEWFLG.EQ.3.OR.NEWFLG.EQ.4) CALL OUTETP
C
      IF(IMFLAG.NE.1) CALL OUTIMP
C
      IF(ATPFLG.EQ.2.OR.PRSFLG.EQ.2.OR.PRGFLG.EQ.2) CALL OUTATP
C
      IF(RLFLAG.EQ.2) CALL OUTSTG
C
      IF(LOCFLG.EQ.2) CALL OUTLAP
C
      IF(DSFLAG.EQ.2) CALL OUTGSF(ICY)
C
      CALL OUTMTH
C
      WRITE(IOUREP,200)
  200 FORMAT('0',80('_'))
C
      CALL USDPTR
C
      RETURN
      END
