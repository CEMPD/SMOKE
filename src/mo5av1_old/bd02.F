     H   2020,0000,0000,0000,0000,0000,0000,0000,
C  CO
C  Low
     L   0000,1977,0000,0000,1979,0000,2020,0000,0000,0000,
     L   0000,0000,0000,0000,0000,0000,0000,0000,
C  High
     H   1977,0000,0000,0000,0000,0000,0000,0000,1979,0000,
     H   2020,0000,0000,0000,0000,0000,0000,0000,
C  NOX
C  Low
     L   0000,1977,0000,0000,0000,0000,0000,0000,0000,0000,
     L   0000,0000,0000,2020,0000,0000,0000,0000,
C  High
     H   1977,0000,0000,0000,0000,0000,0000,0000,0000,0000,
     H   0000,2020,0000,0000,0000,0000,0000,0000/
C
C  Myg upper bounds for the A/X+B & A+B*X+C*X**2 scf equation coefficients.
C
C                  HC    CO   NOX
      DATA MYGSP2/
     1            1979, 1979, 1979,
     2            1980, 1980, 1980,
     3            1980, 1980, 1980/
C
