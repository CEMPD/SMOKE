C  IDLHI<IV>(15,3) - IDLHI<IV> ( IG, IP )
C
C  Local array dictionary:
C
C   Name       Type                Descriptions
C  ----------  ----  --------------------------------------------------------
C  I<J><K><L>   R    as defined above, the intercept/slope <J> in region <K>
C                    for vehicle type pair <L> of the idle exhaust emission
C                    rate equation
C  IDLLO<IV>   I     low altitude myg upper bounds for IDLE ERs
C  IDLHI<IV>   I     high altitude myg upper bounds for IDLE ERs
C
C  Notes:
C
C  None.
C
