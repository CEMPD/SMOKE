      SUBROUTINE ADJUST
C
C  ADJUST interpolates the two Jan 1st values via the evaluation month.
C
C  Called by MOBILE.
C
C  Calls ZCALC.
C
C  Input on call:
C
C    common blocks:
C    /EVAL/   MEVAL
C    /MAXIMA/ MAXVEH,MAXPOL
C    /X1/ XEVP,XGREVP,XVGREV,XGRPD,
C    /X2/ XFTP,XEXH,XEVAP,XREFL,XRUNL,
C    /X3/ XRLG,XRSTG,XRSTL,XIDLE,
C    /X4/ XVFTP,XVEXH,XVEVAP,XVLOSS,XVRUNL,XVRSTL,XVIDLE,
C    /X5/ XREG,XVMT
C
C  Output on return:
C
C    common blocks:
C    /EVAPGR/ EVP,GREVP,VGREVP,PARD,GRPD,
C    /RESUL1/ EFFTP,EFEXH,EFEVAP,EFREFL,EFRUNL
C    /RESUL2/ RLGGAL,RSTGPH,EFRSTL,EFIDLE,
C    /RESUL/ VFTP,VEXH,VEVAP,VLOSS,VRUNLS,VRSTLS,VIDLE
C    /VMXCOM/ REGMIX,TFNORM,VMTMIX,VCOUNT,
C
C  Notes:
C
C  ADJUST was added for MOBILE5v1.
C
C
      INCLUDE 'EVAL.I'
      INCLUDE 'EVAPGR.I'
      INCLUDE 'RESUL1.I'
      INCLUDE 'RESUL2.I'
      INCLUDE 'RESUL3.I'
      INCLUDE 'MAXIMA.I'
      INCLUDE 'VMXCOM.I'
C
      INCLUDE 'X1.I'
      INCLUDE 'X2.I'
      INCLUDE 'X3.I'
      INCLUDE 'X4.I'
      INCLUDE 'X5.I'
C
      MAXV = MAXVEH + 1
      Z = (MEVAL - 1.) / 12.
C
      DO 20 IP = 1, MAXPOL
        VFTP(IP) = ZCALC(XVFTP(IP,1),XVFTP(IP,2),Z)
        VIDLE(IP) = ZCALC(XVIDLE(IP,1),XVIDLE(IP,2),Z)
        DO 10 IV = 1, MAXV
          EFFTP(IP,IV) = ZCALC(XFTP(IP,IV,1),XFTP(IP,IV,2),Z)
          EFIDLE(IP,IV) = ZCALC(XIDLE(IP,IV,1),XIDLE(IP,IV,2),Z)
   10   CONTINUE
   20 CONTINUE
C
      DO 30 IV = 1, MAXV
        EFEXH(IV) = ZCALC(XEXH(IV,1),XEXH(IV,2),Z)
        EFEVAP(IV) = ZCALC(XEVAP(IV,1),XEVAP(IV,2),Z)
        EFREFL(IV) = ZCALC(XREFL(IV,1),XREFL(IV,2),Z)
        EFRUNL(IV) = ZCALC(XRUNL(IV,1),XRUNL(IV,2),Z)
        EFRSTL(IV) = ZCALC(XRSTL(IV,1),XRSTL(IV,2),Z)
        RLGGAL(IV) = ZCALC(XRLG(IV,1),XRLG(IV,2),Z)
        RSTGPH(IV) = ZCALC(XRSTG(IV,1),XRSTG(IV,2),Z)
        EFEXH(IV) = ZCALC(XEXH(IV,1),XEXH(IV,2),Z)
   30 CONTINUE
C
      DO 40 IV = 1, MAXVEH
        REGMIX(IV) = ZCALC(XREG(IV,1),XREG(IV,2),Z)
        VMTMIX(IV) = ZCALC(XVMT(IV,1),XVMT(IV,2),Z)
   40 CONTINUE
C
      VEVAP = ZCALC(XVEVAP(1),XVEVAP(2),Z)
      VLOSS = ZCALC(XVLOSS(1),XVLOSS(2),Z)
      VRUNLS = ZCALC(XVRUNL(1),XVRUNL(2),Z)
      VRSTLS = ZCALC(XVRSTL(1),XVRSTL(2),Z)
      VEXH = ZCALC(XVEXH(1),XVEXH(2),Z)
C
      DO 60 IEVP = 1, 4
        EVP(IEVP) = ZCALC(XEVP(IEVP,1),XEVP(IEVP,2),Z)
        VGREVP(IEVP) = ZCALC(XVGREV(IEVP,1),XVGREV(IEVP,2),Z)
        DO 50 IV = 1, MAXV
          GREVP(IEVP,IV) = ZCALC(XGREVP(IEVP,IV,1),XGREVP(IEVP,IV,2),Z)
   50   CONTINUE
   60 CONTINUE
C
      DO 80 IV = 1, MAXV
        DO 70 IPARTL = 1, 3
          GRPD(IPARTL,IV) = ZCALC(XGRPD(IPARTL,IV,1),XGRPD(IPARTL,IV,2),
     1    Z)
   70   CONTINUE
   80 CONTINUE
C
      RETURN
      END
