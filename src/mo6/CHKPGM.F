   16 IF(CRPRS.NE.CRATP) CALL QUITER(0.,1,108,INERR) 
      IF(PRSFQT.NE.ATPFQT) CALL QUITER(0.,1,109,INERR) 
      IF(PRSPGM.NE.ATPPGM) CALL QUITER(0.,1,110,INERR) 
      GOTO 95 
C 
   17 IF(CRPRS.NE.CRPRG.OR.CRPRG.NE.CRATP.OR.CRPRS.NE.CRATP) 
     * CALL QUITER(0.,1,108,INERR) 
      IF(PRSFQT.NE.PRGFQT.OR.PRGFQT.NE.ATPFQT.OR.PRSFQT.NE.ATPFQT) 
     * CALL QUITER(0.,1,109,INERR) 
      IF(PRSPGM.NE.PRGPGM.OR.PRGPGM.NE.ATPPGM.OR.PRSPGM.NE.ATPPGM) 
     * CALL QUITER(0.,1,110,INERR) 
      GOTO 95 
C 
   19 IF(CRIM(1).NE.CRPRG) CALL QUITER(0.,1,108,INERR) 
      IF(IFREQ(1).NE.PRGFQT) CALL QUITER(0.,1,109,INERR) 
      IF(INTYP(1).NE.PRGPGM) CALL QUITER(0.,1,110,INERR) 
      GOTO 95 
C 
   20 IF(CRIM(1).NE.CRPRS) CALL QUITER(0.,1,108,INERR) 
      IF(IFREQ(1).NE.PRSFQT) CALL QUITER(0.,1,109,INERR) 
      IF(INTYP(1).NE.PRSPGM) CALL QUITER(0.,1,110,INERR) 
      GOTO 95 
C 
   21 IF(CRIM(1).NE.CRPRS.OR.CRIM(1).NE.CRPRG.OR.CRPRS.NE.CRPRG) 
     * CALL QUITER(0.,1,108,INERR) 
      IF(IFREQ(1).NE.PRSFQT.OR.IFREQ(1).NE.PRGFQT.OR. 
     * PRSFQT.NE.PRGFQT) CALL QUITER(0.,1,108,INERR) 
      IF(INTYP(1).NE.PRSPGM.OR.INTYP(1).NE.PRGPGM.OR. 
     * PRSPGM.NE.PRGPGM) CALL QUITER(0.,1,109,INERR) 
      GOTO 95 
C 
   22 IF(CRIM(1).NE.CRATP) CALL QUITER(0.,1,108,INERR) 
      IF(IFREQ(1).NE.ATPFQT) CALL QUITER(0.,1,109,INERR) 
      IF(INTYP(1).NE.ATPPGM) CALL QUITER(0.,1,110,INERR) 
      GOTO 95 
C 
   23 IF(CRIM(1).NE.CRATP.OR.CRIM(1).NE.CRPRG.OR.CRATP.NE.CRPRG) 
     * CALL QUITER(0.,1,108,INERR) 
      IF(IFREQ(1).NE.ATPFQT.OR.IFREQ(1).NE.PRGFQT.OR. 
     * ATPFQT.NE.PRGFQT) CALL QUITER(0.,1,108,INERR) 
      IF(INTYP(1).NE.ATPPGM.OR.INTYP(1).NE.PRGPGM.OR. 
     * ATPPGM.NE.PRGPGM) CALL QUITER(0.,1,109,INERR) 
      GOTO 95 
C 
   24 IF(CRIM(1).NE.CRATP.OR.CRIM(1).NE.CRPRS.OR.CRATP.NE.CRPRS) 
     * CALL QUITER(0.,1,108,INERR) 
      IF(IFREQ(1).NE.ATPFQT.OR.IFREQ(1).NE.PRSFQT.OR. 
     * ATPFQT.NE.PRSFQT) CALL QUITER(0.,1,109,INERR) 
      IF(INTYP(1).NE.ATPPGM.OR.INTYP(1).NE.PRSPGM.OR. 
     * ATPPGM.NE.PRSPGM) CALL QUITER(0.,1,110,INERR) 
      GOTO 95 
C 
   25 IF(CRIM(1).NE.CRATP.OR.CRIM(1).NE.CRPRS.OR.CRATP.NE.CRPRS.OR. 
     * CRPRG.NE.CRATP.OR.CRPRG.NE.CRIM(1).OR.CRPRG.NE.CRPRS) 
     * CALL QUITER(0.,1,108,INERR) 
      IF(IFREQ(1).NE.ATPFQT.OR.IFREQ(1).NE.PRSFQT.OR.ATPFQT.NE.PRSFQT 
     * .OR.PRGFQT.NE.ATPFQT.OR.PRGFQT.NE.IFREQ(1).OR.PRGFQT.NE.PRSFQT) 
     * CALL QUITER(0.,1,109,INERR) 
      IF(INTYP(1).NE.ATPPGM.OR.INTYP(1).NE.PRSPGM.OR.ATPPGM.NE.PRSPGM 
     * .OR.PRGPGM.NE.ATPPGM.OR.PRGPGM.NE.INTYP(1).OR.PRGPGM.NE.PRSPGM) 
     * CALL QUITER(0.,1,110,INERR) 
      GOTO 95 
C 
   26 IF(CRIM(1).NE.CRIM(2)) CALL QUITER(0.,1,108,INERR) 
      IF(IFREQ(1).NE.IFREQ(2)) CALL QUITER(0.,1,109,INERR) 
      IF(INTYP(1).NE.INTYP(2)) CALL QUITER(0.,1,110,INERR) 
      GOTO 95 
C 
   27 IF(CRIM(1).NE.CRIM(2).OR.CRIM(1).NE.CRPRG.OR.CRIM(2).NE.CRPRG) 
     * CALL QUITER(0.,1,108,INERR) 
      IF(IFREQ(1).NE.IFREQ(2).OR.IFREQ(1).NE.PRGFQT.OR.IFREQ(2) 
     * .NE.PRGFQT) CALL QUITER(0.,1,109,INERR) 
      IF(INTYP(1).NE.INTYP(2).OR.INTYP(1).NE.PRGPGM.OR.INTYP(2) 
     * .NE.PRGPGM) CALL QUITER(0.,1,110,INERR) 
      GOTO 95 
C 
   28 IF(CRIM(1).NE.CRIM(2).OR.CRIM(1).NE.CRPRS.OR.CRIM(2).NE.CRPRS) 
     * CALL QUITER(0.,1,108,INERR) 
      IF(IFREQ(1).NE.IFREQ(2).OR.IFREQ(1).NE.PRSFQT.OR.IFREQ(2) 
     * .NE.PRSFQT) CALL QUITER(0.,1,109,INERR) 
      IF(INTYP(1).NE.INTYP(2).OR.INTYP(1).NE.PRSPGM.OR.INTYP(2) 
     * .NE.PRSPGM) CALL QUITER(0.,1,110,INERR) 
      GOTO 95 
C 
   29 IF(CRIM(1).NE.CRIM(2).OR.CRIM(1).NE.CRPRS.OR.CRIM(2).NE.CRPRG.OR. 
     * CRPRG.NE.CRIM(1).OR.CRPRG.NE.CRIM(2).OR.CRPRG.NE.CRPRS) 
     * CALL QUITER(0.,1,108,INERR) 
      IF(IFREQ(1).NE.IFREQ(2).OR.IFREQ(1).NE.PRSFQT.OR.IFREQ(2) 
     * .NE.PRSFQT.OR.PRGFQT.NE.IFREQ(1).OR.PRGFQT.NE.IFREQ(2) 
     * .OR.PRGFQT.NE.PRSFQT) CALL QUITER(0.,1,109,INERR) 
      IF(INTYP(1).NE.INTYP(2).OR.INTYP(1).NE.PRSPGM.OR.INTYP(2) 
     * .NE.PRSPGM.OR.PRGPGM.NE.INTYP(1).OR.PRGPGM.NE.INTYP(2) 
     * .OR.PRGPGM.NE.PRSPGM) CALL QUITER(0.,1,110,INERR) 
      GOTO 95 
C 
   30 IF(CRIM(1).NE.CRIM(2).OR.CRIM(1).NE.CRATP.OR.CRIM(2).NE.CRATP) 
     * CALL QUITER(0.,1,108,INERR) 
      IF(IFREQ(1).NE.IFREQ(2).OR.IFREQ(1).NE.ATPFQT.OR.IFREQ(2) 
     * .NE.ATPFQT) CALL QUITER(0.,1,109,INERR) 
      IF(INTYP(1).NE.INTYP(2).OR.INTYP(1).NE.ATPPGM.OR.INTYP(2) 
     * .NE.ATPPGM) CALL QUITER(0.,1,110,INERR) 
      GOTO 95 
C 
   31 IF(CRIM(1).NE.CRIM(2).OR.CRIM(1).NE.CRATP.OR.CRIM(2).NE.CRPRG.OR. 
     * CRPRG.NE.CRIM(1).OR.CRPRG.NE.CRIM(2).OR.CRPRG.NE.CRATP) 
     * CALL QUITER(0.,1,108,INERR) 
      IF(IFREQ(1).NE.IFREQ(2).OR.IFREQ(1).NE.ATPFQT.OR.IFREQ(2) 
     * .NE.ATPFQT.OR.PRGFQT.NE.IFREQ(1).OR.PRGFQT.NE.IFREQ(2) 
     * .OR.PRGFQT.NE.ATPFQT) CALL QUITER(0.,1,109,INERR) 
      IF(INTYP(1).NE.INTYP(2).OR.INTYP(1).NE.ATPPGM.OR.INTYP(2) 
     * .NE.ATPPGM.OR.PRGPGM.NE.INTYP(1).OR.PRGPGM.NE.INTYP(2) 
     * .OR.PRGPGM.NE.ATPPGM) CALL QUITER(0.,1,110,INERR) 
      GOTO 95 
C 
   32 IF(CRIM(1).NE.CRIM(2).OR.CRIM(1).NE.CRATP.OR.CRIM(2).NE.CRPRS.OR. 
     * CRPRS.NE.CRIM(1).OR.CRPRS.NE.CRIM(2).OR.CRPRS.NE.CRATP) 
     * CALL QUITER(0.,1,108,INERR) 
      IF(IFREQ(1).NE.IFREQ(2).OR.IFREQ(1).NE.ATPFQT.OR.IFREQ(2) 
     * .NE.ATPFQT.OR.PRSFQT.NE.IFREQ(1).OR.PRSFQT.NE.IFREQ(2) 
     * .OR.PRSFQT.NE.ATPFQT) CALL QUITER(0.,1,109,INERR) 
      IF(INTYP(1).NE.INTYP(2).OR.INTYP(1).NE.ATPPGM.OR.INTYP(2) 
     * .NE.ATPPGM.OR.PRSPGM.NE.INTYP(1).OR.PRSPGM.NE.INTYP(2) 
     * .OR.PRSPGM.NE.ATPPGM) CALL QUITER(0.,1,110,INERR) 
      GOTO 95 
C 
   33 IF(CRIM(1).NE.CRIM(2).OR.CRIM(1).NE.CRATP.OR.CRIM(1).NE.CRPRG.OR. 
     * CRIM(1).NE.CRPRS.OR.CRIM(1).NE.CRPRG.OR.CRIM(2).NE.CRATP.OR. 
     * CRIM(2).NE.CRPRS.OR.CRIM(2).NE.CRPRG.OR.CRATP.NE.CRPRS.OR. 
     * CRATP.NE.CRPRG.OR.CRPRS.NE.CRPRG) CALL QUITER(0.,1,108,INERR) 
      IF(IFREQ(1).NE.IFREQ(2).OR.IFREQ(1).NE.ATPFQT.OR.IFREQ(1).NE. 
     * PRSFQT.OR.IFREQ(1).NE.PRGFQT.OR.IFREQ(2).NE.ATPFQT.OR.IFREQ(2) 
     * .NE.PRSFQT.OR.IFREQ(2).NE.PRGFQT.OR.ATPFQT.NE.PRSFQT.OR.ATPFQT 
     * .NE.PRGFQT.OR.PRSFQT.NE.PRGFQT) CALL QUITER(0.,1,109,INERR) 
      IF(INTYP(1).NE.INTYP(2).OR.INTYP(1).NE.ATPPGM.OR.INTYP(1).NE. 
     * PRSPGM.OR.INTYP(1).NE.PRGPGM.OR.INTYP(2).NE.ATPPGM.OR.INTYP(2) 
     * .NE.PRSPGM.OR.INTYP(2).NE.PRGPGM.OR.ATPPGM.NE.PRSPGM.OR.ATPPGM 
     * .NE.PRGPGM.OR.PRSPGM.NE.PRGPGM) CALL QUITER(0.,1,110,INERR) 
   95 RETURN 
      END 
