      INCLUDE 'ATPAR2.I' 
      INCLUDE 'FLAGS2.I' 
      INCLUDE 'FLAGS3.I' 
      INCLUDE 'IMPAR1.I' 
      INCLUDE 'IMPAR6.I' 
      INCLUDE 'PRGCH2.I' 
      INCLUDE 'PRSCH2.I' 
