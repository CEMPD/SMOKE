      SUBROUTINE CHKPGM(INERR) 
C 
C  CHKPGM decide which programs are applicable: I/M, AT, Pressure and/or 
C  Purge.  If they are applicable then it checks whether compliance 
C  rates, inspection frequency and/or inspection types are different. 
C 
C  Called by CHKINP 
C 
C  Calls QUITER 
C 
